package model;

/**
 *
 * @author duongvu
 */
public class Contact {
    int id;
    String icon, link;

    public Contact() {
    }

    public Contact(int id, String icon, String link) {
        this.id = id;
        this.icon = icon;
        this.link = link;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
    
}
